#include "libft/libft.h"

void	ft_put_oct(const long unsigned int n)
{
	if (n >= 8)
	{
		ft_putnbr(n / 8);
		ft_putchar(n % 8 + '0');
	}
	else
		ft_putchar(n + '0');
}
